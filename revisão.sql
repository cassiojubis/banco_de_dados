-- FUNÇÕES DE DATAS
-- DATE_ ADD -->  ADD UMA DATA MEDIANTE DE UM INTERVALO(DIA[DAY], MESES[MONYH], ANO[YEAR]
-- CURDATE() -->  RETORNAR A  DATA ATUAL 
-- DATE_SUB() --> SUBTRAI DATAS MEDIANTE DE UM INTERVALO(DIA[DAY], MESES[MONYH], ANO[YEAR]
-- DAY/ MONTH / YEAR --> RETORNA A DATA SEPARADA
-- DATEDIFF --> RETORNA A DIFERENÇA (EM DIAS) ENTRE DUAS DATAS
-- DAYOFFWEEK -->   TRAS O DIA DA SEMENA DE 1 A 7
-- DAYOFFMONTH --> TRAS O MES DE 1 A 12
-- DAYOFYEAR -->  TRAS O DIA DO ANO  
-- MONTHNAME --> RETORNA O NOME DO MÊS
-- DAYNAME --> RETORNA O NOME DO DIA
-- NOW() --> RETORNA A DATA E A HORA
 -- LAST DAY RETORNA O ÚLTIMO DIA DO MÊS 
SELECT 
	cand_nome as "CANDIDATO",
    cand_numero as "NUMERO"
FROM candidato
where cand_foto is null;


-- count
SELECT 
	COUNT(*) AS" QDT_FOTO_NULA"
FROM candidato
where cand_foto is  NOT null;

SELECT 
	COUNT(*) AS" QDT_FOTO_NULA"
FROM candidato
where cand_foto is null;

select 
COUNT(*)  AS "QTD_CANDIDATOS",
	sum(cand_numero) as "NUMERO",
    AVG(cand_numero) as "MÈDIA",
    min(cand_numero) as "Menor Num",
    max(cand_numero) as "Maior Num"
from candidato;

--  HAVING  SERVE PARA  FILTRAR AS AGREGAÇÕES
SELECT 
part_numero  as"Partido",
count(*) as "QTD_CANDIDATOS"
FROM candidato
WHERE cand_nome like "%A"
group  by part_numero
having COUNT(*) > 2;

select 
	count(*) AS "QTD_VOTOS",
    cand_numero  as "numero do candidato"
from voto
group  by cand_numero
having  count(*) = 2;

