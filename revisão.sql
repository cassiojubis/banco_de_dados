-- DAY/ MONTH / YEAR
SELECT CURDATE() as "DATA ATUAL",
DAY(CURDATE()) AS  "DIA",
MONTH(CURDATE())AS "MÊS",
YEAR(CURDATE()) AS "ANO";

-- DATEDIFF
SELECT DATEDIFF(CURDATE(),"2008-07-24") / 365 AS "DIFERENÇA EM DIAS";

-- DAYOFWEEK 
SELECT DAYOFWEEK("2008-07-24") AS "DIA DA SEMANA";

-- DAYOFMONTH
